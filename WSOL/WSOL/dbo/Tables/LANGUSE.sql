    ON [dbo].[LANGUSE]([IDLANGLIST] ASC);


GO
CREATE NONCLUSTERED INDEX [XIF1LANGUSE]
    ON [dbo].[LANGUSE]([IDAPPLICANT] ASC);


GO
CREATE UNIQUE NONCLUSTERED INDEX [ROWID$INDEX]
    ON [dbo].[LANGUSE]([ROWID] ASC);


GO
