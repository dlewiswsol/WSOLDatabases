CREATE TABLE [dbo].[IDAPPSTATUSHISTORY] (
    [IDAPPSTATUSHISTORY] NUMERIC (20)   NOT NULL,
    [IDAPPLICANT]        NUMERIC (20)   NOT NULL,
    [IDAPPSTATUS]        NUMERIC (20)   NOT NULL,
    [COMMENTS]           VARCHAR (4000) NOT NULL,
    [IDUSER]             NUMERIC (20)   NOT NULL,
