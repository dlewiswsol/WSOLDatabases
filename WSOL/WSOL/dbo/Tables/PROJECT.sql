    [IDPROJECT]   NUMERIC (20)     NOT NULL,
    [NAME]        VARCHAR (250)    NOT NULL,
    [IDCLIENT]    NUMERIC (20)     NOT NULL,
    [IDPAYTYPE]   NUMERIC (20)     NOT NULL,
    [IDLINEOFBUS] NUMERIC (20)     NOT NULL,
