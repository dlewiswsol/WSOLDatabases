    CONSTRAINT [IDAPPSTATUSHISTORY_C03] FOREIGN KEY ([IDAPPSTATUS]) REFERENCES [dbo].[APPSTATUS] ([IDAPPSTATUS]),
    CONSTRAINT [IDAPPSTATUSHISTORY_C04] FOREIGN KEY ([IDUSER]) REFERENCES [dbo].[USERS] ([IDUSER])
);






GO
CREATE NONCLUSTERED INDEX [IDAPPSTATUSHISTORY_I02]
    ON [dbo].[IDAPPSTATUSHISTORY]([IDAPPSTATUS] ASC, [IDUSER] ASC, [CHANGEDDATE] ASC);


GO
