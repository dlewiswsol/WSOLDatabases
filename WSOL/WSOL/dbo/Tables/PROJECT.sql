    [DATEUPDATED] DATETIME2 (0)    NOT NULL,
    [ISSHOW]      CHAR (1)         DEFAULT ('Y') NOT NULL,
    [ISSHOWLB]    CHAR (1)         DEFAULT ('Y') NOT NULL,
    [ROWID]       UNIQUEIDENTIFIER DEFAULT (newid()) NOT NULL,
    CONSTRAINT [SYS_C0013138] PRIMARY KEY CLUSTERED ([IDPROJECT] ASC)
);




