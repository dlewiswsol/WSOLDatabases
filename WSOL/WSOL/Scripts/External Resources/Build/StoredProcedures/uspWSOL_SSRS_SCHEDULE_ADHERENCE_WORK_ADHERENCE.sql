INSERT INTO #WSOL_FUNCTION_WORK_DETAILS
(
  DATE_TIME
 ,HOUR_INTERVAL
 ,CLIENT_ID
 ,CLIENT_NAME
-- ,SCHEDULER_ACL_NAME
 ,RESOURCE_NAME
 ,CORPORATE_NAME
 ,IDAGENT
 ,CATS_ID
 ,EMAIL_ADDRESS
 --,INVOICE_MAP_KEY
 ,LOGGED_IN_TIME
 ,PRODUCTIVE_TIME
)
SELECT
  RESULT.DATE_TIME
 ,RESULT.HOUR_INTERVAL
 ,RESULT.CLIENT_ID
 ,CLI.[NAME]
 --,RESULT.SCHEDULER_ACL_NAME
 ,MAX(RESULT.RESOURCE_NAME)
 ,APP.CORPORATNAME
 ,MAP.IDAGENT
 ,APP.AGENTIDENTNUM
 ,APP.EMAIL
 --,RESULT.INVOICE_MAP_KEY
 ,SUM(RESULT.LOGGED_IN_TIME)				AS LOGGED_IN_TIME
 ,SUM(RESULT.PRODUCTIVE_TIME)				AS PRODUCTIVE_TIME
FROM
 TVF_WSOL_WORK_DATA_BY_INTERVALS(@DTM_BEG, @DTM_END, 'H', 'S', @CLIENT_ID, @PROGRAM_ID) RESULT	--<<<<< DEFAULTING TO HOURLY INTERVALS IN SECONDS, WILL AGGREGATE BELOW
 LEFT JOIN #WSOL_FUNCTION_MANUAL_MAP_DATA MAP ON RESULT.INVOICE_MAP_KEY = MAP.FIELD_MAP_KEY
											 AND RESULT.CLIENT_ID = MAP.CLIENT_ID
 LEFT JOIN CLIENT CLI		ON RESULT.CLIENT_ID = CLI.ID
 LEFT JOIN AGENTS AGT		ON MAP.IDAGENT		= AGT.ID
 LEFT JOIN APPLICANT APP	ON AGT.EMAIL		= APP.EMAIL
--WHERE MAP.FIELD_MAP_KEY IS NOT NULL
GROUP BY
  RESULT.DATE_TIME
 ,RESULT.HOUR_INTERVAL
 ,RESULT.CLIENT_ID
 ,CLI.[NAME]
 --,RESULT.SCHEDULER_ACL_NAME
 --,RESULT.RESOURCE_NAME
 ,APP.CORPORATNAME
 ,MAP.IDAGENT
 ,APP.AGENTIDENTNUM
 ,APP.EMAIL
 --,RESULT.INVOICE_MAP_KEY

