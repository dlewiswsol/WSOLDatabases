			  AND ( S.DATEINS >= @DTM_INS															   ) 
			  AND ( @CLIENT_ID	IN (',0,') OR CHARINDEX(',' + RTRIM(C.ID)	   + ',',@CLIENT_ID) > 0   ) --C.ID = @CLIENT_ID
			  AND ( @PROGRAM_ID IN (',0,') OR CHARINDEX(',' + RTRIM(S.IDPROGR) + ',',@PROGRAM_ID)> 0   ) -- P.ID = @PROGRAM_ID
			GROUP BY
			 S.IDAGENT
			,S.DATESCH
			,S.IDPROGR
			,C.ID
			,C.[NAME]
			,P.[NAME]

			UNION ALL

			SELECT
			 S.IDAGENT															AS IDAGENT
