 ,COMMITTED_TIME
 ,CANCELLED_TIME
 ,SCHEDULED_TIME
 ,RESOURCE_NAME
)
SELECT
  RESULT.AGENT_FIRST_NAME
 ,RESULT.AGENT_MIDDLE_NAME
 ,RESULT.AGENT_LAST_NAME
 ,RESULT.CORPORATE_NAME
 ,RESULT.IDAGENT
 ,RESULT.CATS_ID
 ,RESULT.EMAIL_ADDRESS
 ,RESULT.SCHEDULE_DATE
 ,RESULT.INTERVAL_START
 ,RESULT.SCHEDULE_DATE_INT
 ,RESULT.CLIENT_ID
 ,RESULT.CLIENT_NAME
 --,RESULT.PROGRAM_ID
 --,RESULT.[PROGRAM_NAME]
 ,SUM(RESULT.SCHEDULED_TIME)								AS COMMITTED_TIME
 ,SUM(RESULT.CANCELLED_TIME)								AS CANCELLED_TIME
 ,SUM(RESULT.SCHEDULED_TIME) + SUM(RESULT.CANCELLED_TIME)	AS SCHEDULED_TIME
 ,RESULT.RESOURCE_NAME
FROM
 TVF_WSOL_SCHEDULE_DATA_BY_INTERVALS(@DTM_BEG, @DTM_END, 'H', 'S', @CLIENT_ID, @PROGRAM_ID) RESULT	--<<<<< DEFAULTING TO HOURLY INTERVALS IN SECONDS, WILL AGGREGATE BELOW
GROUP BY
 RESULT.AGENT_FIRST_NAME
,RESULT.AGENT_MIDDLE_NAME
,RESULT.AGENT_LAST_NAME
,RESULT.CORPORATE_NAME
,RESULT.IDAGENT
,RESULT.CATS_ID
,RESULT.EMAIL_ADDRESS
,RESULT.SCHEDULE_DATE
,RESULT.INTERVAL_START
,RESULT.SCHEDULE_DATE_INT
,RESULT.CLIENT_ID
,RESULT.CLIENT_NAME
,RESULT.RESOURCE_NAME

