 --================================================================================================
 --	CREATE TEMP TABLE TO HOLD RESULTS
 --================================================================================================
 IF OBJECT_ID('TEMPDB..#WSOL_SSRS_SCHEDULE_ADHERENCE_WORK_ADHERENCE') IS NOT NULL BEGIN
   DROP TABLE #WSOL_SSRS_SCHEDULE_ADHERENCE_WORK_ADHERENCE
END
CREATE TABLE #WSOL_SSRS_SCHEDULE_ADHERENCE_WORK_ADHERENCE
(RESOURCE_NAME				VARCHAR(300)
,CORPORATE_NAME				VARCHAR(500)
,IDAGENT					VARCHAR(20)
,CATS_ID					VARCHAR(20)
,EMAIL_ADDRESS				VARCHAR(250)
,DATE_TIME					DATETIME
,INTERVAL_START				VARCHAR(2)
,CLIENT_ID					VARCHAR(20)
,CLIENT_NAME				VARCHAR(150)
,COMMITTED_TIME				DECIMAL(10,2)
,CANCELLED_TIME				DECIMAL(10,2)
,SCHEDULED_TIME				DECIMAL(10,2)
,LOGGED_IN_TIME				DECIMAL(10,2)
,PRODUCTIVE_TIME			DECIMAL(10,2)
,SCHEDULE_ADHERENCE			DECIMAL(10,4)
,WORK_ADHERENCE				DECIMAL(10,4)
,NO_SHOW_TIME				DECIMAL(10,2)
,UNSCHEDULED_TIME			DECIMAL(10,2)
,CANCELLED_PERCENT			DECIMAL(10,4)
,CANCELLED_PERCENT_MODIFIED	DECIMAL(10,4)
)

INSERT INTO #WSOL_SSRS_SCHEDULE_ADHERENCE_WORK_ADHERENCE
SELECT
 DAT.RESOURCE_NAME
,DAT.CORPORATE_NAME
,DAT.IDAGENT
,DAT.CATS_ID
,DAT.EMAIL_ADDRESS
,DAT.DATE_TIME
,DAT.INTERVAL_START
,DAT.CLIENT_ID
,DAT.CLIENT_NAME
,DAT.COMMITTED_TIME
,DAT.CANCELLED_TIME
,DAT.SCHEDULED_TIME
,DAT.LOGGED_IN_TIME
,DAT.PRODUCTIVE_TIME
,DAT.SCHEDULE_ADHERENCE
,DAT.WORK_ADHERENCE
,DAT.NO_SHOW_TIME
,DAT.UNSCHEDULED_TIME
,DAT.CANCELLED_PERCENT
,DAT.CANCELLED_PERCENT_MODIFIED
FROM
(	
	SELECT
	 ISNULL(LTRIM(RTRIM(SCH.RESOURCE_NAME))		,LTRIM(RTRIM(INV.RESOURCE_NAME)))		AS RESOURCE_NAME
	,ISNULL(SCH.CORPORATE_NAME		,INV.CORPORATE_NAME)	AS CORPORATE_NAME
	,ISNULL(SCH.IDAGENT				,INV.IDAGENT)			AS IDAGENT
	,ISNULL(SCH.CATS_ID				,INV.CATS_ID)			AS CATS_ID
	,ISNULL(SCH.EMAIL_ADDRESS		,INV.EMAIL_ADDRESS)		AS EMAIL_ADDRESS
	,ISNULL(SCH.SCHEDULE_DATE_INT	,INV.DATE_TIME)			AS DATE_TIME
	
	,/*CASE WHEN @INTERVAL = 'H' THEN */
			CASE WHEN DATEPART(HOUR,ISNULL(SCH.SCHEDULE_DATE_INT,INV.DATE_TIME)) < 10 THEN
				   '0' + CONVERT(VARCHAR(1), DATEPART(HOUR,ISNULL(SCH.SCHEDULE_DATE_INT,INV.DATE_TIME)))
				 ELSE
				   CONVERT(VARCHAR(2),DATEPART(HOUR,ISNULL(SCH.SCHEDULE_DATE_INT,INV.DATE_TIME)))
			END
		/*  ELSE '00'
	 END	*/												AS INTERVAL_START

	,ISNULL(SCH.CLIENT_ID			,INV.CLIENT_ID)			AS CLIENT_ID
	,ISNULL(SCH.CLIENT_NAME			,INV.CLIENT_NAME)		AS CLIENT_NAME
	,ISNULL(SUM(SCH.COMMITTED_TIME)	,0.00)					AS COMMITTED_TIME
	,ISNULL(SUM(SCH.CANCELLED_TIME)	,0.00)					AS CANCELLED_TIME
	,ISNULL(SUM(SCH.SCHEDULED_TIME)	,0.00)					AS SCHEDULED_TIME
	,ISNULL(SUM(INV.LOGGED_IN_TIME)	,0.00)					AS LOGGED_IN_TIME
	,ISNULL(SUM(INV.PRODUCTIVE_TIME),0.00)					AS PRODUCTIVE_TIME
	,NULL													AS SCHEDULE_ADHERENCE
	 /*CASE WHEN ISNULL(SUM(SCH.SCHEDULED_TIME),0.00) > 0.00 THEN
			( ISNULL(SUM(INV.LOGGED_IN_TIME),0.00) / ISNULL(SUM(SCH.SCHEDULED_TIME),0.00) )		--* 100.00
		  ELSE 0.00
	 END													AS SCHEDULE_ADHERENCE */
	,NULL													AS WORK_ADHERENCE
	 /*CASE WHEN ISNULL(SUM(SCH.COMMITTED_TIME),0.00) > 0.00 THEN
			( ISNULL(SUM(INV.PRODUCTIVE_TIME),0.00) / ISNULL(SUM(SCH.COMMITTED_TIME),0.00) )	--* 100.00
		  ELSE 0.00
	 END													AS WORK_ADHERENCE */
	,CASE WHEN ( ISNULL(SUM(SCH.COMMITTED_TIME),0.00) - ISNULL(SUM(INV.LOGGED_IN_TIME),0.00) ) > 0.00 THEN
			ISNULL(SUM(SCH.COMMITTED_TIME),0.00) - ISNULL(SUM(INV.LOGGED_IN_TIME),0.00)
		  ELSE NULL
	 END													AS NO_SHOW_TIME
	,CASE WHEN ( ISNULL(SUM(INV.LOGGED_IN_TIME),0.00) - ISNULL(SUM(SCH.COMMITTED_TIME),0.00) ) > 0.00 THEN
			ISNULL(SUM(INV.LOGGED_IN_TIME),0.00) - ISNULL(SUM(SCH.COMMITTED_TIME),0.00)
		  ELSE NULL
	 END													AS UNSCHEDULED_TIME
	,NULL													AS CANCELLED_PERCENT
	 /*CASE WHEN ISNULL(SUM(SCH.SCHEDULED_TIME),0.00) > 0.00 THEN
			CASE WHEN ISNULL(SUM(SCH.CANCELLED_TIME),0.00) > 0.00 THEN
				   ( ISNULL(SUM(SCH.CANCELLED_TIME),0.00) / ISNULL(SUM(SCH.SCHEDULED_TIME),0.00) )		--* 100.00
				 ELSE NULL END
		  ELSE NULL
	 END													AS CANCELLED_PERCENT */
	,NULL													AS CANCELLED_PERCENT_MODIFIED 
	 /*CASE WHEN ISNULL(SUM(SCH.SCHEDULED_TIME),0.00) > 0.00 THEN
			CASE WHEN ( ISNULL(SUM(SCH.COMMITTED_TIME),0.00) - ISNULL(SUM(INV.LOGGED_IN_TIME),0.00) ) > 0.00 THEN
				   ( ISNULL(SUM(SCH.CANCELLED_TIME),0.00) + ISNULL(SUM(SCH.COMMITTED_TIME),0.00) - ISNULL(SUM(INV.LOGGED_IN_TIME),0.00) ) / ISNULL(SUM(SCH.SCHEDULED_TIME),0.00)
				 ELSE NULL
			END
				   
		  ELSE NULL
	 END													AS CANCELLED_PERCENT_MODIFIED */

FROM #WSOL_FUNCTION_SCHEDULE_DETAILS SCH
FULL OUTER JOIN #WSOL_FUNCTION_WORK_DETAILS	INV ON SCH.SCHEDULE_DATE_INT	= INV.DATE_TIME
											   AND SCH.IDAGENT				= INV.IDAGENT
											   AND SCH.CLIENT_ID			= INV.CLIENT_ID
GROUP BY
 ISNULL(LTRIM(RTRIM(SCH.RESOURCE_NAME))		,LTRIM(RTRIM(INV.RESOURCE_NAME)))
,ISNULL(SCH.CORPORATE_NAME		,INV.CORPORATE_NAME)
,ISNULL(SCH.IDAGENT				,INV.IDAGENT)
,ISNULL(SCH.CATS_ID				,INV.CATS_ID)
,ISNULL(SCH.EMAIL_ADDRESS		,INV.EMAIL_ADDRESS)
,ISNULL(SCH.SCHEDULE_DATE_INT	,INV.DATE_TIME)
,ISNULL(SCH.CLIENT_ID			,INV.CLIENT_ID)
,ISNULL(SCH.CLIENT_NAME			,INV.CLIENT_NAME)
) DAT

