			,0.00																AS CANCELLED_SECS
			FROM  SCHTIMES_AGENT	S
			LEFT JOIN PROGRAM		P ON S.IDPROGR	= P.ID
			LEFT JOIN CLIENT		C ON P.IDCLIENT = C.ID
