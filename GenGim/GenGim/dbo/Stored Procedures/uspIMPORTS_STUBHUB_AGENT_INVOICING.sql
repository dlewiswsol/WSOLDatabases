,INCLUDE_IN_CAS         varchar(1)
,READY_FOR_XML          varchar(1)
,SHOW_ID_EXTS           varchar(1)
,
[LOGGED_IN_TIME] [int] NULL,
[READY_TIME] [int] NULL,
[NOT_READY_TIME] [int] NULL,
[CALLS_ACCEPTED] [int] NULL,
