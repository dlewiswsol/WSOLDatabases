			,CAST(CONVERT(VARCHAR(10), S.DATESCH) + ' 01:30:00' AS DATETIME)	AS INTERVAL_START
			,CASE WHEN SUM(S.F0130) > 0 THEN 900 ELSE 0 END						AS SCHEDULED_SECS
